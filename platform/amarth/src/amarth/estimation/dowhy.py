"""Causal effect estimation using DoWhy."""

from dataclasses import dataclass
from typing import Optional

import networkx as nx
import pandas as pd
from dowhy import CausalModel
import structlog

from amarth.discovery.ensemble import EdgeStatus

logger = structlog.get_logger(__name__)


@dataclass
class CausalEstimateResult:
    """Container for the results of a DoWhy causal estimation."""

    treatment: str
    outcome: str
    ate: float
    edge_status: str
    is_significant: bool
    refutation_passed: bool
    method_name: str
    summary: str
    p_value: float | None
    stderr: float | None
    ci_lower: float | None
    ci_upper: float | None


class DowhyEstimator:
    """Estimates the causal effect of a treatment on an outcome using DoWhy.

    Acts as a bridge: sanitizes the discovered NetworkX DAG, initializes DoWhy,
    identifies the estimand, estimates the effect, and refutes the model.
    """

    def __init__(self, strict_dag: bool = True):
        """Initializes the DoWhy bridge.

        Args:
            strict_dag: If True, aggressively prunes uncertain edges to ensure
                        the graph is a DAG before passing it to DoWhy.
        """
        self.strict_dag = strict_dag

    def estimate_effect(
        self,
        df: pd.DataFrame,
        dag: nx.DiGraph,
        treatment: str,
        outcome: str,
        method_name: str = "backdoor.linear_regression",
    ) -> Optional[CausalEstimateResult]:
        """Estimates the ATE of treatment on outcome.

        Args:
            df: Input dataset.
            dag: The causal graph generated by the discovery phase.
            treatment: The name of the intervention variable.
            outcome: The name of the target variable.
            method_name: The DoWhy estimation method to use.

        Returns:
            A CausalEstimateResult object, or None if no causal path exists.
        """
        clean_dag = self._sanitize_graph(dag)

        if not nx.has_path(clean_dag, treatment, outcome):
            logger.info("no_causal_path", treatment=treatment, outcome=outcome)
            return None

        edge_status = (
            dag[treatment][outcome].get("status", "unknown")
            if dag.has_edge(treatment, outcome)
            else "indirect"
        )

        model = CausalModel(
            data=df, treatment=treatment, outcome=outcome, graph=clean_dag
        )

        try:
            identified_estimand = model.identify_effect(
                proceed_when_unidentifiable=False
            )
        except ValueError as e:
            logger.warning("unidentifiable_effect", error=str(e))
            return None

        estimate = model.estimate_effect(
            identified_estimand, method_name=method_name, test_significance=True
        )

        refute = model.refute_estimate(
            identified_estimand, estimate, method_name="random_common_cause"
        )
        refutation_passed = refute.new_effect is not None

        p_value = getattr(estimate, "p_value", None)
        if not isinstance(p_value, (float, int)):
            p_value = None

        stderr = getattr(estimate, "stderr", None)
        if not isinstance(stderr, (float, int)):
            stderr = None

        ci_lower: float | None = None
        ci_upper: float | None = None
        get_ci = getattr(estimate, "get_confidence_intervals", None)
        if callable(get_ci):
            try:
                ci = get_ci()
                if (
                    isinstance(ci, (list, tuple))
                    and len(ci) >= 2
                    and isinstance(ci[0], (float, int))
                    and isinstance(ci[1], (float, int))
                ):
                    ci_lower = float(ci[0])
                    ci_upper = float(ci[1])
            except Exception as exc:
                logger.debug("confidence_interval_unavailable", error=str(exc))

        return CausalEstimateResult(
            treatment=treatment,
            outcome=outcome,
            ate=float(estimate.value),
            edge_status=edge_status,
            is_significant=estimate.test_stat_significance()
            if hasattr(estimate, "test_stat_significance")
            else True,
            refutation_passed=refutation_passed,
            method_name=method_name,
            summary=str(estimate),
            p_value=float(p_value) if p_value is not None else None,
            stderr=float(stderr) if stderr is not None else None,
            ci_lower=ci_lower,
            ci_upper=ci_upper,
        )

    def _sanitize_graph(self, dag: nx.DiGraph) -> nx.DiGraph:
        """Removes cycles and uncertain edges to satisfy DoWhy requirements."""
        clean_dag = dag.copy()

        if self.strict_dag:
            edges_to_remove = [
                (u, v)
                for u, v, d in clean_dag.edges(data=True)
                if d.get("status") == EdgeStatus.CONFLICT_DIRECTION.value
            ]
            clean_dag.remove_edges_from(edges_to_remove)

        while not nx.is_directed_acyclic_graph(clean_dag):
            try:
                cycle = nx.find_cycle(clean_dag)
                weakest_edge = min(
                    cycle,
                    key=lambda e: abs(clean_dag[e[0]][e[1]].get("weight", 1.0)),
                )
                clean_dag.remove_edge(*weakest_edge)
                logger.debug("cycle_broken", removed_edge=weakest_edge)
            except nx.NetworkXNoCycle:
                break

        return clean_dag
